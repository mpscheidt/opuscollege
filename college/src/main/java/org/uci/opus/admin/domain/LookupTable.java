package org.uci.opus.admin.domain;

/**
 * @author smacumbe
 *
 */
public class LookupTable {
    
    public static final String COUNTRY = "country";
    public static final String DISTRICT = "district";
    public static final String PROVINCE = "province";
    public static final String ADMINISTRATIVEPOST = "administrativepost";

	private int id;
	private String tableName;
	private String lookupType;
	private String active;
	private TableDependency[] dependencies;
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the tableName
	 */
	public String getTableName() {
		return tableName;
	}
	/**
	 * @param tableName the tableName to set
	 */
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	/**
	 * @return the lookupType
	 */
	public String getLookupType() {
		return lookupType;
	}
	/**
	 * @param lookupType the lookupType to set
	 */
	public void setLookupType(String lookupType) {
		this.lookupType = lookupType;
	}
	/**
	 * @return the active
	 */
	public String getActive() {
		return active;
	}
	/**
	 * @param active the active to set
	 */
	public void setActive(String active) {
		this.active = active;
	}
	/**
	 * @return the dependencies
	 */
	public TableDependency[] getDependencies() {
		return dependencies;
	}
	/**
	 * @param dependencies the dependencies to set
	 */
	public void setDependencies(TableDependency[] dependencies) {
		this.dependencies = dependencies;
	}
	
	
	

}
