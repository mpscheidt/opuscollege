package org.uci.opus.college.domain;

import java.io.Serializable;
import java.util.Date;

public class AppConfigAttribute implements Serializable {

    private static final long serialVersionUID = 1L;

    private String appConfigAttributeName;
    private Integer branchId;
    private String appConfigAttributeValue;
    private Date startDate;
    private Date endDate;

    /**
     * @return the appConfigAttributeName
     */
    public String getAppConfigAttributeName() {
        return appConfigAttributeName;
    }

    /**
     * @param appConfigAttributeName
     *            the appConfigAttributeName to set
     */
    public void setAppConfigAttributeName(String appConfigAttributeName) {
        this.appConfigAttributeName = appConfigAttributeName;
    }

    /**
     * @return the appConfigAttributeValue
     */
    public String getAppConfigAttributeValue() {
        return appConfigAttributeValue;
    }

    /**
     * @param appConfigAttributeValue
     *            the appConfigAttributeValue to set
     */
    public void setAppConfigAttributeValue(String appConfigAttributeValue) {
        this.appConfigAttributeValue = appConfigAttributeValue;
    }

    /**
     * @return the startDate
     */
    public Date getStartDate() {
        return startDate;
    }

    /**
     * @param startDate
     *            the startDate to set
     */
    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    /**
     * @return the endDate
     */
    public Date getEndDate() {
        return endDate;
    }

    /**
     * @param endDate
     *            the endDate to set
     */
    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

    public Integer getBranchId() {
        return branchId;
    }

    public void setBranchId(Integer branchId) {
        this.branchId = branchId;
    }

}
